Sementara itu Humas PT Kristalin Ekalestari Maria Erari menerangkan sudah kesekian kalinya pihaknya menyerahkan rumah untuk masyarakat yang belum memiliki rumah dan sudah mendata secara detail sekaligus rekomendasikan dari Ketua Adat.

"Ini bukti komitmen kami sebagai perusahaan yang bergerak di Tambang, memberikan manfaat banyak kepada masyarakat terutama warga yang belum mendapatkan rumah lewat program CSR," ujar Maria Erari dalam keterangannya di Jakarta, Selasa 24 Juni 2025.

Menurut Maria, ia mengilustrasikan Desa Nifasi ini sebagai seorang wanita berubah menjadi cantik yang terawat. "Kami ibaratkan Desa ini sebelumnya biasa-biasa saja, kini menjadi berubah seperti wanita cantik yang terawat dan memberikan manfaat banyak kepada masyarakat dengan kehadiran kami PT Kristalin Ekalestari," katanya.

